    private void excluirBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirBtnActionPerformed

        if (jtConsumidores.getSelectedRow() != -1) {
            Consumidor p = new Consumidor();
            ConsumidorDB dao = new ConsumidorDB();

            p.setId((int)jtConsumidores.getValueAt(jtConsumidores.getSelectedRow(), 0));

            dao.excluir(p);

            txtNome.setText("");
            txtEmail.setText("");
            txtIdade.setText("");
            txtEndereco.setText("");

            listarJTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir");
        }

    }//GEN-LAST:event_excluirBtnActionPerformed

    private void atualizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarBtnActionPerformed
        if (jtConsumidores.getSelectedRow() != 1) {
            int temp1 = obterIdRegiaoSelecionada();
            Consumidor p = new Consumidor();
            ConsumidorDB dao = new ConsumidorDB();
            p.setNome(txtNome.getText());
            p.setEmail(txtEmail.getText());
            p.setIdade(Integer.parseInt(txtIdade.getText()));
            p.setEndereco(txtEndereco.getText());
            p.setId((int)jtConsumidores.getValueAt(jtConsumidores.getSelectedRow(), 0));
            p.setRegiao_id(temp1);
            dao.atualizar(p);

            txtNome.setText("");
            txtEmail.setText("");
            txtIdade.setText("");
            txtEndereco.setText("");

            listarJTabela();
        }
    }//GEN-LAST:event_atualizarBtnActionPerformed

