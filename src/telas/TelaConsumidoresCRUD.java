    private int obterIdRegiaoSelecionada() {
        Regiao regiaoSelecionada = (Regiao) cbRegioes.getSelectedItem();
        if (regiaoSelecionada != null) {
            return regiaoSelecionada.getId();
        } else {
            // Lidar com a situação em que nenhum item está selecionado
            return -1; // Ou outro valor padrão apropriado
        }
    }
    
        public void listarJTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jtConsumidores.getModel();
        modelo.setNumRows(0);  
            ConsumidorDB pdao = new ConsumidorDB();

            for (Consumidor atual : pdao.listar()) {

                modelo.addRow(new Object[]{
                    atual.getId(),
                    atual.getNome(),
                    atual.getIdade(),
                    atual.getEmail(),
                    atual.getEndereco(),
                    atual.getUf()
                });

            }

        }
        
        public void listarJTabelaEmOrdemCrescentePorNome() {
        DefaultTableModel modelo = (DefaultTableModel) jtConsumidores.getModel();
        modelo.setNumRows(0);  
            ConsumidorDB pdao = new ConsumidorDB();

            for (Consumidor atual : pdao.listarEmOrdemCrescentePorNome()) {

                modelo.addRow(new Object[]{
                    atual.getId(),
                    atual.getNome(),
                    atual.getIdade(),
                    atual.getEmail(),
                    atual.getEndereco(),
                    atual.getUf()
                });

            }

        }
        
        public void listarJTabelaPorRegiao(String regiao) {
        DefaultTableModel modelo = (DefaultTableModel) jtConsumidores.getModel();
        modelo.setNumRows(0);  
            ConsumidorDB pdao = new ConsumidorDB();

            for (Consumidor atual : pdao.listarConsumidorPorRegiao(regiao)) {

                modelo.addRow(new Object[]{
                    atual.getId(),
                    atual.getNome(),
                    atual.getIdade(),
                    atual.getEmail(),
                    atual.getEndereco(),
                    atual.getUf()
                });

            }

        }

